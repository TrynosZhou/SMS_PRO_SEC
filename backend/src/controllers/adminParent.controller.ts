import { Response } from 'express';
import bcrypt from 'bcryptjs';
import { randomBytes } from 'crypto';
import { AppDataSource } from '../config/database';
import { User, UserRole } from '../entities/User';
import { Parent } from '../entities/Parent';
import { Student } from '../entities/Student';
import { AuthRequest } from '../middleware/auth';

const generateTemporaryPassword = () => {
  return `Temp-${randomBytes(4).toString('hex')}-${Date.now().toString().slice(-4)}`;
};

/** Admin: List all parents with stats */
export const listParents = async (req: AuthRequest, res: Response) => {
  try {
    if (!req.user || (req.user.role !== UserRole.ADMIN && req.user.role !== UserRole.SUPERADMIN)) {
      return res.status(403).json({ message: 'Admin access required' });
    }

    if (!AppDataSource.isInitialized) await AppDataSource.initialize();

    const { search } = req.query;
    const parentRepository = AppDataSource.getRepository(Parent);
    const studentRepository = AppDataSource.getRepository(Student);

    let qb = parentRepository
      .createQueryBuilder('parent')
      .leftJoinAndSelect('parent.students', 'students')
      .orderBy('parent.firstName', 'ASC')
      .addOrderBy('parent.lastName', 'ASC');

    if (search && typeof search === 'string' && search.trim()) {
      const term = `%${search.trim()}%`;
      qb = qb.andWhere(
        '(parent.firstName ILIKE :term OR parent.lastName ILIKE :term OR parent.email ILIKE :term OR parent.phoneNumber ILIKE :term)',
        { term }
      );
    }

    const parents = await qb.getMany();

    // Stats
    const notLinked = parents.filter((p) => !p.students || p.students.length === 0).length;
    const linkedStudentsCount = parents.reduce((sum, p) => sum + (p.students?.length || 0), 0);
    const studentsWithAccounts = await studentRepository
      .createQueryBuilder('s')
      .where('s.userId IS NOT NULL')
      .getCount();

    const items = parents.map((p) => ({
      id: p.id,
      firstName: p.firstName,
      lastName: p.lastName,
      email: p.email,
      phoneNumber: p.phoneNumber,
      address: p.address,
      gender: p.gender,
      userId: p.userId,
      studentsCount: p.students?.length || 0,
      students: (p.students || []).map((s) => ({
        id: s.id,
        firstName: s.firstName,
        lastName: s.lastName,
        studentNumber: s.studentNumber,
        hasLoginAccount: !!s.userId,
      })),
    }));

    res.json({
      parents: items,
      stats: {
        total: items.length,
        notLinked,
        linkedStudents: linkedStudentsCount,
        studentAccounts: studentsWithAccounts,
      },
    });
  } catch (error: any) {
    console.error('listParents error:', error);
    res.status(500).json({ message: 'Server error', error: error.message });
  }
};

/** Admin: Create parent account (User + Parent entity) */
export const createParentAccount = async (req: AuthRequest, res: Response) => {
  try {
    if (!req.user || (req.user.role !== UserRole.ADMIN && req.user.role !== UserRole.SUPERADMIN)) {
      return res.status(403).json({ message: 'Admin access required' });
    }

    if (!AppDataSource.isInitialized) await AppDataSource.initialize();

    const {
      firstName,
      lastName,
      email,
      phoneNumber,
      address,
      gender,
      password,
      generatePassword = true,
    } = req.body || {};

    if (!firstName || !lastName || !email) {
      return res.status(400).json({ message: 'First name, last name, and email are required' });
    }

    const emailStr = String(email).trim();
    const emailPattern = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    if (!emailPattern.test(emailStr)) {
      return res.status(400).json({ message: 'Please enter a valid email address' });
    }

    const userRepository = AppDataSource.getRepository(User);
    const parentRepository = AppDataSource.getRepository(Parent);

    const trimmedEmail = emailStr.toLowerCase();
    const existingUser = await userRepository.findOne({ where: { email: trimmedEmail } });
    if (existingUser) {
      return res.status(400).json({ message: 'Email already exists' });
    }

    const baseUsername = trimmedEmail.split('@')[0].replace(/\s+/g, '');
    let finalUsername = baseUsername;
    let suffix = 1;
    while (await userRepository.findOne({ where: { username: finalUsername } })) {
      finalUsername = `${baseUsername}${suffix++}`;
    }

    let plainPassword = password ? String(password).trim() : '';
    const autoGenerated = !plainPassword;
    if (!plainPassword) plainPassword = generateTemporaryPassword();
    if (plainPassword.length < 8) {
      return res.status(400).json({ message: 'Password must be at least 8 characters' });
    }

    const hashedPassword = await bcrypt.hash(plainPassword, 10);
    const user = userRepository.create({
      email: trimmedEmail,
      username: finalUsername,
      password: hashedPassword,
      role: UserRole.PARENT,
      mustChangePassword: autoGenerated,
      isTemporaryAccount: autoGenerated,
      isActive: true,
    });
    await userRepository.save(user);

    const parent = parentRepository.create({
      firstName: String(firstName).trim(),
      lastName: String(lastName).trim(),
      email: trimmedEmail,
      phoneNumber: phoneNumber ? String(phoneNumber).trim() : null,
      address: address ? String(address).trim() : null,
      gender: gender ? String(gender).trim() : null,
      userId: user.id,
    });
    await parentRepository.save(parent);

    res.status(201).json({
      message: 'Parent account created successfully',
      parent: {
        id: parent.id,
        firstName: parent.firstName,
        lastName: parent.lastName,
        email: parent.email,
      },
      user: { id: user.id, username: user.username },
      temporaryCredentials: autoGenerated ? { password: plainPassword } : undefined,
    });
  } catch (error: any) {
    console.error('createParentAccount error:', error);
    res.status(500).json({ message: 'Server error', error: error.message });
  }
};

/** Admin: Reset parent password */
export const resetParentPassword = async (req: AuthRequest, res: Response) => {
  try {
    if (!req.user || (req.user.role !== UserRole.ADMIN && req.user.role !== UserRole.SUPERADMIN)) {
      return res.status(403).json({ message: 'Admin access required' });
    }

    if (!AppDataSource.isInitialized) await AppDataSource.initialize();

    const { id } = req.params;
    const { newPassword, generatePassword = false } = req.body;

    if (!id) return res.status(400).json({ message: 'Parent ID is required' });

    let plainPassword: string;
    if (generatePassword) {
      plainPassword = generateTemporaryPassword();
    } else {
      if (!newPassword || newPassword.length < 8) {
        return res.status(400).json({ message: 'New password (min 8 chars) is required when not generating' });
      }
      plainPassword = String(newPassword);
    }

    const parentRepository = AppDataSource.getRepository(Parent);
    const userRepository = AppDataSource.getRepository(User);

    const parent = await parentRepository.findOne({ where: { id } });
    if (!parent) return res.status(404).json({ message: 'Parent not found' });
    if (!parent.userId) return res.status(400).json({ message: 'Parent has no login account' });

    const user = await userRepository.findOne({ where: { id: parent.userId } });
    if (!user) return res.status(404).json({ message: 'User not found' });

    user.password = await bcrypt.hash(plainPassword, 10);
    user.mustChangePassword = generatePassword;
    user.isTemporaryAccount = generatePassword;
    await userRepository.save(user);

    res.json({
      message: 'Parent password reset successfully',
      ...(generatePassword && { temporaryPassword: plainPassword }),
    });
  } catch (error: any) {
    console.error('resetParentPassword error:', error);
    res.status(500).json({ message: 'Server error', error: error.message });
  }
};

