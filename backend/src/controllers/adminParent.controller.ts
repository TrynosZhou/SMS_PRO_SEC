const EMAIL_PATTERN = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;

/** Admin: Update parent */
export const updateParent = async (req: AuthRequest, res: Response) => {
  try {
    if (!req.user || (req.user.role !== UserRole.ADMIN && req.user.role !== UserRole.SUPERADMIN)) {
      return res.status(403).json({ message: 'Admin access required' });
    }

    const { id } = req.params;
    const { firstName, lastName, email, phoneNumber, address, gender } = req.body || {};

    const parentRepository = AppDataSource.getRepository(Parent);
    const userRepository = AppDataSource.getRepository(User);

    const parent = await parentRepository.findOne({ where: { id } });
    if (!parent) return res.status(404).json({ message: 'Parent not found' });

    if (firstName !== undefined) parent.firstName = String(firstName).trim();
    if (lastName !== undefined) parent.lastName = String(lastName).trim();

    if (email !== undefined) {
      const emailVal = email ? String(email).trim() : '';
      if (emailVal) {
        if (!EMAIL_PATTERN.test(emailVal)) {
          return res.status(400).json({ message: 'Please enter a valid email address' });
        }
        const trimmedEmail = emailVal.toLowerCase();
        const existingUser = await userRepository.findOne({ where: { email: trimmedEmail } });
        if (existingUser && existingUser.id !== parent.userId) {
          return res.status(400).json({ message: 'Email is already in use by another account' });
        }
        parent.email = trimmedEmail;
      } else {
        parent.email = null;
      }
    }
    if (phoneNumber !== undefined) parent.phoneNumber = phoneNumber ? String(phoneNumber).trim() : null;
    if (address !== undefined) parent.address = address ? String(address).trim() : null;
    if (gender !== undefined) parent.gender = gender ? String(gender).trim() : null;

    await parentRepository.save(parent);

    if (parent.userId && (email !== undefined || parent.email)) {
      const user = await userRepository.findOne({ where: { id: parent.userId } });
      if (user && email !== undefined) {
        user.email = parent.email;
        await userRepository.save(user);
      }
    }

    res.json({ message: 'Parent updated successfully', parent });
  } catch (error: any) {
    console.error('updateParent error:', error);
    res.status(500).json({ message: 'Server error', error: error.message });
  }
};

/** Admin: Delete parent (unlinks students, removes parent and user) */
export const deleteParent = async (req: AuthRequest, res: Response) => {
  try {
    if (!req.user || (req.user.role !== UserRole.ADMIN && req.user.role !== UserRole.SUPERADMIN)) {
      return res.status(403).json({ message: 'Admin access required' });
    }

    const { id } = req.params;
    const parentRepository = AppDataSource.getRepository(Parent);
    const studentRepository = AppDataSource.getRepository(Student);
    const userRepository = AppDataSource.getRepository(User);

    const parent = await parentRepository.findOne({ where: { id } });
    if (!parent) return res.status(404).json({ message: 'Parent not found' });

    // Unlink all students
    await studentRepository.update({ parentId: id }, { parentId: null });

    const userId = parent.userId;

    // Delete parent
    await parentRepository.remove(parent);

    // Delete associated user if exists
    if (userId) {
      await userRepository.delete(userId);
    }

    res.json({ message: 'Parent account deleted successfully' });
  } catch (error: any) {
    console.error('deleteParent error:', error);
    res.status(500).json({ message: 'Server error', error: error.message });
  }
};

