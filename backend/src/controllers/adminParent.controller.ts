/** Admin: Bulk link students to parent */
export const adminBulkLinkStudents = async (req: AuthRequest, res: Response) => {
  try {
    if (!req.user || (req.user.role !== UserRole.ADMIN && req.user.role !== UserRole.SUPERADMIN)) {
      return res.status(403).json({ message: 'Admin access required' });
    }

    const { id } = req.params;
    const { studentIds } = req.body;

    if (!Array.isArray(studentIds) || studentIds.length === 0) {
      return res.status(400).json({ message: 'studentIds array with at least one ID is required' });
    }

    const parentRepository = AppDataSource.getRepository(Parent);
    const studentRepository = AppDataSource.getRepository(Student);

    const parent = await parentRepository.findOne({ where: { id } });
    if (!parent) return res.status(404).json({ message: 'Parent not found' });

    const linked: any[] = [];
    const skipped: { id: string; reason: string }[] = [];

    for (const studentId of studentIds) {
      if (!studentId || typeof studentId !== 'string') continue;
      const student = await studentRepository.findOne({
        where: { id: studentId },
        relations: ['classEntity'],
      });
      if (!student) {
        skipped.push({ id: studentId, reason: 'Student not found' });
        continue;
      }
      if (student.parentId === parent.id) {
        skipped.push({ id: studentId, reason: 'Already linked' });
        continue;
      }
      if (student.parentId && student.parentId !== parent.id) {
        skipped.push({ id: studentId, reason: 'Linked to another parent' });
        continue;
      }
      student.parentId = parent.id;
      await studentRepository.save(student);
      linked.push({
        id: student.id,
        firstName: student.firstName,
        lastName: student.lastName,
        studentNumber: student.studentNumber,
        classEntity: student.classEntity,
      });
    }

    res.json({
      message: linked.length > 0 ? `${linked.length} student(s) linked successfully` : 'No students linked',
      linked,
      skipped: skipped.length > 0 ? skipped : undefined,
    });
  } catch (error: any) {
    console.error('adminBulkLinkStudents error:', error);
    res.status(500).json({ message: 'Server error', error: error.message });
  }
};

